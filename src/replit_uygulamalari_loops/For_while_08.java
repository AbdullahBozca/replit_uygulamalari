package replit_uygulamalari_loops;

public class For_while_08 {

	public static void main(String[] args) {
		// 'a' ile 'z' arasındaki karakterlerin ascii değerlerini 
		//yazdıran bir Java programı yazın.
		for (char i = 'a'; i <='z'; i++) {
			System.out.println("Ascii value of "+i+" = "+(i+0));
		}

	}

}
